import math
PI = 3.14
# Redondear
print(round(PI))

# Redondear hacia arriba
print(math.ceil(PI))

# Redondear hacia abajo
print(math.floor(PI))

# Valor absoluto
print(abs(PI))

# Elevar un numero base a una potencia
print(pow(PI,2))

# Raiz Cuadrada
print(math.sqrt(16))
print()

