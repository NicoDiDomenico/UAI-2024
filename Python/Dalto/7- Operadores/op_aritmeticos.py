#Suma y Resta (+ y -)
suma = 18 + 12
resta = 18 - 12

#Multiplicacion y Division (* y /)
mult = 2 * 2
div = 13 / 2  #devuelve un float

#Potenciacion (exponente, **)
exponte = 2 ** 5

#Division baja (//)
div_baja = 13 // 2  #devuelve entero redondeado hacia abajo

#Resto o modulo (%) 
resto = 4 % 2

tipo_dato = type(resto)
print(tipo_dato)

print(suma)
print(resta)
print(mult)
print(div)
print(exponte)
print(div_baja)
print(resto)